var age = 25;
if (age < 0) {
    console.log("Invalid age");
}
else if (age < 5) {
    console.log("Infant");
}
else if (age < 13) {
    console.log("Child");
}
else if (age < 20) {
    console.log("Teenager");
}
else if (age < 60) {
    console.log("Adult");
}
else {
    console.log("Senior Citizen");
}
